<template>
  <div :class="suitClasses" :style="{'top': offset}">
      <div :class="numberClasses">
      </div>
    </div>
</template>

<script>
  export default {
    name: 'card',
    props: ['card', 'index'],
    computed: {
      suitClasses () {
        var obj = {
          card: true,
          'selectedCard': this.card.selected
        }

        if (this.card) {
          obj[this.card.cardSuit] = true
        }

        return obj
      },
      numberClasses () {
        var obj = {
          cardsize: true
        }

        if (this.card) {
          obj['_' + this.card.cardNumber] = true
        }

        return obj
      },
      offset () {
        return (this.index * 25) + 'px'
      }
    }
  }
</script>
